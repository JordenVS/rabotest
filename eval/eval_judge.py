# ===========================================================================
# Judge prompts  (verbatim from Appendix B)
# ===========================================================================

_CORRECTNESS_PROMPT = """\
You are an evaluator assessing the correctness of an answer to a \
process mining question.
You will be given:
- A question about a Procure-to-Pay business process
- A gold answer representing the ground truth
- A predicted answer generated by an AI system
Your task is to judge whether the predicted answer is semantically \
correct with respect to the gold answer. A prediction is correct if \
it conveys the same factual content as the gold answer, even if \
phrased differently. A prediction is incorrect if it states a \
different activity, a different object, or expresses uncertainty \
where the gold answer is definitive.
Question: {question}
Gold answer: {gold_answer}
Predicted answer: {prediction}
Respond with a JSON object containing exactly two fields:
- "correct": true or false
- "reason": a single sentence explaining your judgement
Do not output anything else."""

_FAITHFULNESS_PROMPT = """\
You are an evaluator assessing whether an AI-generated answer is \
faithful to the context it was given.
You will be given:
- A question about a Procure-to-Pay business process
- The context provided to the AI system to answer the question
- A predicted answer generated by the AI system
Your task is to judge whether every claim in the predicted answer \
is supported by the provided context. A prediction is faithful if \
all factual claims it makes can be traced to information present \
in the context. A prediction is unfaithful if it introduces any \
activity name, object identifier, relationship, or factual claim \
that does not appear in the context, regardless of whether that \
claim happens to be correct.
Question: {question}
Context: {context}
Predicted answer: {prediction}
Respond with a JSON object containing exactly three fields:
- "faithful": true or false
- "hallucinated_content": a brief description of any hallucinated \
  claims, or null if the prediction is faithful
- "reason": a single sentence explaining your judgement
Do not output anything else."""

# ===========================================================================
# OpenAI judge
# ===========================================================================

def _call_judge(
    prompt: str,
    client,
    model: str,
    retries: int = 3,
    retry_delay: float = 5.0,
) -> Tuple[Dict, Dict]:
    """
    Call the judge model and parse the JSON response.
    Retries on transient API errors.

    Returns (parsed_dict, token_meta).
    """
    for attempt in range(retries):
        try:
            response = client.chat.completions.create(
                model=model,
                messages=[
                    {
                        "role": "system",
                        "content": (
                            "You are a strict evaluator. "
                            "Always respond with valid JSON only."
                        ),
                    },
                    {"role": "user", "content": prompt},
                ],
                temperature=0.0,
                # Response format enforces JSON output — available on
                # gpt-4o and gpt-4o-mini (OpenAI, 2024).
                response_format={"type": "json_object"},
            )
            token_meta = {
                "judge_prompt_tokens":  response.usage.prompt_tokens,
                "judge_completion_tokens": response.usage.completion_tokens,
            }
            content = response.choices[0].message.content or "{}"
            parsed = json.loads(content)
            return parsed, token_meta

        except json.JSONDecodeError as e:
            print(f"  [Judge] JSON parse error (attempt {attempt+1}): {e}")
        except Exception as e:
            print(f"  [Judge] API error (attempt {attempt+1}): {e}")
            if attempt < retries - 1:
                time.sleep(retry_delay)

    return {}, {"judge_prompt_tokens": None, "judge_completion_tokens": None}


def judge_correctness(
    question: str,
    gold_answer: str,
    prediction: str,
    client,
    model: str,
) -> Tuple[Optional[bool], str, Dict]:
    """
    Returns (correct: bool | None, reason: str, token_meta: dict).
    None indicates a parse failure — treated as NaN in aggregation.
    """
    prompt = _CORRECTNESS_PROMPT.format(
        question=question,
        gold_answer=gold_answer,
        prediction=prediction,
    )
    parsed, token_meta = _call_judge(prompt, client, model)
    correct = parsed.get("correct")          # may be True, False, or None
    reason  = parsed.get("reason", "")
    return correct, reason, token_meta


def judge_faithfulness(
    question: str,
    context: str,
    prediction: str,
    client,
    model: str,
) -> Tuple[Optional[bool], Optional[str], str, Dict]:
    """
    Returns (faithful: bool | None, hallucinated_content: str | None,
             reason: str, token_meta: dict).
    """
    if not context:
        # Cannot judge faithfulness without context — skip gracefully
        return None, None, "No context available for this system.", {}

    prompt = _FAITHFULNESS_PROMPT.format(
        question=question,
        context=context,
        prediction=prediction,
    )
    parsed, token_meta = _call_judge(prompt, client, model)
    faithful             = parsed.get("faithful")
    hallucinated_content = parsed.get("hallucinated_content")
    reason               = parsed.get("reason", "")
    return faithful, hallucinated_content, reason, token_meta


# ===========================================================================
# Aggregation helpers (extends metrics.aggregate for judge-specific fields)
# ===========================================================================

JUDGE_METRIC_COLS = [
    "n",
    "correctness",        # mean of correct (0/1)
    "faithfulness",       # mean of faithful (0/1)
    "hallucination_rate", # 1 - faithfulness
    "judge_prompt_tokens_mean",
]


def aggregate_judge(scored: List[Dict]) -> Dict[str, Dict]:
    from collections import defaultdict
    import numpy as np

    grouped: Dict[str, List[Dict]] = defaultdict(list)
    for s in scored:
        grouped[s["system"]].append(s)

    results = {}
    for system, recs in grouped.items():

        def _mean(key: str) -> float:
            vals = [
                float(r[key]) for r in recs
                if key in r
                and r[key] is not None
                and not (isinstance(r[key], float) and np.isnan(r[key]))
            ]
            return float(np.mean(vals)) if vals else float("nan")

        faith = _mean("faithful")
        results[system] = {
            "n":                        len(recs),
            "correctness":              _mean("correct"),
            "faithfulness":             faith,
            "hallucination_rate":       1.0 - faith if not (faith != faith) else float("nan"),
            "judge_prompt_tokens_mean": _mean("judge_prompt_tokens"),
        }

    return results


# ===========================================================================
# Main evaluation loop
# ===========================================================================

def run_judge_evaluation(args: argparse.Namespace) -> None:
    from openai import OpenAI
    client = OpenAI()

    os.makedirs(args.out_dir, exist_ok=True)
    verdicts_path = os.path.join(args.out_dir, "judge_verdicts.jsonl")
    done = load_done(verdicts_path)

    # ------------------------------------------------------------------ #
    # Load generated answers
    # ------------------------------------------------------------------ #
    print(f"Loading answers: {args.answers}")
    answers = load_jsonl(args.answers)
    if args.limit:
        # Keep proportional representation across systems
        from collections import defaultdict
        by_system: Dict[str, List] = defaultdict(list)
        for a in answers:
            by_system[a["system"]].append(a)
        answers = [
            rec
            for recs in by_system.values()
            for rec in recs[: args.limit]
        ]
        print(f"  [--limit] Using first {args.limit} instances per system.")

    print(f"  {len(answers)} answer records loaded.\n")

    # ------------------------------------------------------------------ #
    # Evaluation loop
    # ------------------------------------------------------------------ #
    all_scored: List[Dict] = []

    for ans in tqdm(answers, desc="[Judge]"):
        instance_id = ans["instance_id"]
        system      = ans["system"]

        if (instance_id, system) in done:
            continue

        question    = ans.get("question", "")
        gold_answer = ans.get("gold_answer", "")
        prediction  = ans.get("prediction", "")

        # --- Reconstruct context for faithfulness ---
        context = ans.get("context", "")
        # RAG and GraphRAG context is not stored in answers.jsonl;
        # faithfulness will be skipped (None) for these systems unless
        # you extend storage in eval_answers.py to persist the context string.

        print(f"\n[{system}] Instance {instance_id}")

        # --- Semantic correctness ---
        correct, correct_reason, correct_tokens = judge_correctness(
            question, gold_answer, prediction, client, args.judge_model
        )

        # --- Faithfulness ---
        faithful, hallucinated, faith_reason, faith_tokens = judge_faithfulness(
            question, context, prediction, client, args.judge_model
        )

        record = {
            "instance_id":          instance_id,
            "system":               system,
            "question":             question,
            "gold_answer":          gold_answer,
            "prediction":           prediction,
            # correctness
            "correct":              correct,
            "correct_reason":       correct_reason,
            "judge_prompt_tokens":  correct_tokens.get("judge_prompt_tokens"),
            "judge_completion_tokens": correct_tokens.get("judge_completion_tokens"),
            # faithfulness
            "faithful":             faithful,
            "hallucinated_content": hallucinated,
            "faith_reason":         faith_reason,
            "faith_prompt_tokens":  faith_tokens.get("judge_prompt_tokens"),
            "faith_completion_tokens": faith_tokens.get("judge_completion_tokens"),
        }

        append_record(verdicts_path, record)
        done.add((instance_id, system))
        all_scored.append(record)

    # ------------------------------------------------------------------ #
    # Aggregate + report
    # ------------------------------------------------------------------ #
    all_written = load_jsonl(verdicts_path) if os.path.exists(verdicts_path) else []
    results = aggregate_judge(all_written)

    print_results_table(results, JUDGE_METRIC_COLS)

    write_results_table(
        results,
        args.out_dir,
        caption=(
            "LLM-as-a-judge evaluation on P2P OCEL benchmark. "
            "Correctness = fraction of predictions judged semantically correct "
            "by GPT-4o-mini vs. gold answer. "
            "Faithfulness = fraction of predictions containing no hallucinated "
            "claims relative to the provided context. "
            "Hallucination rate = 1 - Faithfulness."
        ),
        label="tab:judge_results",
    )

    # Rename outputs so they don't collide with eval_answers tables
    import shutil
    for ext in ("csv", "tex"):
        src = os.path.join(args.out_dir, f"results_table.{ext}")
        dst = os.path.join(args.out_dir, f"judge_results_table.{ext}")
        if os.path.exists(src):
            shutil.move(src, dst)
            print(f"Renamed → {dst}")

    print(f"\nAll verdicts → {verdicts_path}")


