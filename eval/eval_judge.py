"""
eval2/eval_judge.py
===================
Stage 3 — LLM-as-a-judge evaluation for semantic correctness and faithfulness.

Loads generated answers from eval_answers.py (answers.jsonl), scores each
(question, gold_answer, prediction, context) tuple using GPT-4o-mini as judge,
and writes per-instance verdicts and aggregated results.

Two criteria are evaluated independently, following the prompts defined in
Appendix B of the paper:

  Semantic Correctness — does the prediction convey the same factual content
      as the gold answer, regardless of phrasing?
  Faithfulness — does every claim in the prediction trace to the provided
      context, or does the model hallucinate information?

Note on context availability
-----------------------------
Faithfulness requires the context that was passed to each system. For GCR
systems this is reconstructed from the paths + context_block stored in the
predicted_paths JSONL. For RAG and GraphRAG the context is not stored in
answers.jsonl by default; pass --rag_answers and --graphrag_context so the
script can reconstruct it.

Output files
------------
<out_dir>/judge_verdicts.jsonl     one record per (instance_id, system)
<out_dir>/judge_results.csv        aggregated per-system correctness & faithfulness
<out_dir>/judge_results.tex        LaTeX table

Usage
-----
    python -m eval2.eval_judge \\
        --answers         results/answers.jsonl \\
        --constrained_paths  results/predicted_paths_constrained.jsonl \\
        --unconstrained_paths results/predicted_paths_unconstrained.jsonl \\
        --out_dir         results \\
        --judge_model     gpt-4o-mini \\
        --limit           20

References
----------
Zheng, L., Chiang, W.-L., Sheng, Y., Zhuang, S., Wu, Z., Zhuang, Y., ...
    & Stoica, I. (2023). Judging LLM-as-a-judge with MT-bench and chatbot
    arena. NeurIPS 2023.
Es, S., James, J., Espinosa-Anke, L., & Schockaert, S. (2023).
    RAGAS: Automated evaluation of retrieval augmented generation.
    arXiv:2309.15217.
"""

