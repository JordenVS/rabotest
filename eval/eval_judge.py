# CLI
# ===========================================================================

def parse_args() -> argparse.Namespace:
    p = argparse.ArgumentParser(
        description=(
            "Stage 3: LLM-as-a-judge evaluation of semantic correctness "
            "and faithfulness for all four systems."
        )
    )
    p.add_argument("--answers", required=True,
                   help="answers.jsonl produced by eval_answers.py.")
    p.add_argument("--judge_model", default="gpt-4o-mini",
                   help="OpenAI model to use as judge.")
    p.add_argument("--out_dir", default="results",
                   help="Directory for judge_verdicts.jsonl and judge_results_table.*")
    p.add_argument("--limit", type=int, default=None,
                   help="Evaluate only the first N instances per system.")
    return p.parse_args()


if __name__ == "__main__":
    run_judge_evaluation(parse_args())